package oop;

public class Studentinfo {
    //Encapsulation
    private int age;
    private String name;
    private String DOB;
    public void setAge(int age) {

        this.age = age;
    }
    public int getAge(){

        return age;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }
    public void setDOB(String DOB) {
        this.DOB = DOB;
    }
    public String getDOB() {
        return DOB;
    }
}
