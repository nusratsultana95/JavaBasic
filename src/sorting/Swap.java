package sorting;

public class Swap {
    public static void main(String[] args) {
        int x=10;
        int z=12;
        int temp;
        //goal>>swap the numbers
        System.out.println("x= "+x);
        System.out.println("z= "+z);
        temp=x;
        x=13;
        System.out.println("x= "+x);
        System.out.println("z= "+z);
        System.out.println("temp="+temp);

    }
}
